        <section class="program-info" id="program-info">
            <h3>Program Description</h3>
            <p class="program-description">
                The BLIS Program is the study of the development, deployment, and management of information resources in print, non-print, electronic and digital formats and services. Graduates of this program are being prepared to apply information technology to basic library operations and functions. Moreover, they are trained to harness a range of bibliographical and online tools to support teaching, research, and other services. 

                <br><br>

                Graduates of this program are likewise expected to systematically organize, conserve, preserve and restore information objects (e.g., realia museum pieces), historical and cultural documents (e.g., artifacts, archival documents, indigenous knowledge), and other intellectual proeprties (creative ideas and works by individuals and groups).
                
                <br><br>

                The program was based in the Guidelines for Professional Library/Information Education Programs of the International Federation of the Library Associates and Institutions (IPLA) as well as on the curricula of various library schools abroad.
            </p>
        </section>

